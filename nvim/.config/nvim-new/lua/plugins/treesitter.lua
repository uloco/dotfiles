			"graphql",
			"groovy",
			"html",
			"jsdoc",
			"php",
			"python",
			"ruby",
			"regex",
			"scss",
			"toml",
			"yaml",
			"vim",
			"vimdoc",
			"swift",
			"svelte",
			"rust",
			"r",
			"go",
			"cpp",
