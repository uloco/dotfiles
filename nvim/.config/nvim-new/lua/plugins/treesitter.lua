	"nvim-treesitter/nvim-treesitter",
	branch = "main",
	lazy = false,
	build = ":TSUpdate",
	cmd = { "TSUpdateSync", "TSUpdate", "TSInstall" },
	keys = {
		{ "<C-S-P>", "<cmd>Inspect<cr>", mode = "n", desc = "Highlight Capture" },
	},
	opts = {
		ensure_installed = {
			"bash",
			"typescript",
			"typescriptreact",
			"tsx",
			"javascript",
			"javascriptreact",
